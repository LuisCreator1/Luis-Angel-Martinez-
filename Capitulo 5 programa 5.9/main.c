#include <stdio.h>

/* Búsqueda secuencial en arreglos desordenados. */
const int MAX = 100;

void Lectura(int[], int); /* Prototipos de funciones. */
int Busca(int *, int, int);

int main(void) {
    int RES, ELE, TAM, VEC[MAX];

    do {
        printf("Ingrese el tamaño del arreglo: ");
        scanf("%d", &TAM);
    } while (TAM > MAX || TAM < 1); /* Verificación del tamaño del arreglo. */

    Lectura(VEC, TAM);

    printf("\nIngrese el elemento a buscar: ");
    scanf("%d", &ELE);

    RES = Busca(VEC, TAM, ELE); /* Llama a la función que busca en el arreglo. */

    if (RES)
        printf("\nEl elemento se encuentra en la posición %d", RES);
    else
        printf("\nEl elemento no se encuentra en el arreglo");

    return 0;
}

void Lectura(int A[], int T)
/* La función Lectura se utiliza para leer un arreglo unidimensional de T elementos de tipo entero. */
{
    int I;
    for (I = 0; I < T; I++) {
        printf("Ingrese el elemento %d: ", I + 1);
        scanf("%d", &A[I]);
    }
}

int Busca(int A[], int T, int K)
/* Esta función localiza en el arreglo un elemento determinado. Si el elemento es encontrado,
regresa la posición correspondiente. En caso contrario, regresa 0. */
{
    int I = 0, BAN = 0, RES;
    while (I < T && !BAN) {
        if (A[I] == K)
            BAN++;
        else
            I++;
    }
    if (BAN)
        RES = I + 1; /* Se asigna I+1 dado que las posiciones en el arreglo comienzan desde cero. */
    else
        RES = 0;
    return (RES);
}
